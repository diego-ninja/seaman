<?php

declare(strict_types=1);

// ABOUTME: Builds PHAR executable using Box.
// ABOUTME: Creates distributable seaman.phar in build directory.

namespace Seaman\Command;

use Symfony\Component\Console\Attribute\AsCommand;
use Symfony\Component\Console\Command\Command;
use Symfony\Component\Console\Input\InputInterface;
use Symfony\Component\Console\Output\OutputInterface;
use Symfony\Component\Console\Style\SymfonyStyle;
use Symfony\Component\Process\Process;

#[AsCommand(
    name: 'seaman:build',
    description: 'Build PHAR executable using Box',
    aliases: ['build'],
)]
class BuildCommand extends Command
{
    protected function execute(InputInterface $input, OutputInterface $output): int
    {
        $io = new SymfonyStyle($input, $output);
        $projectRoot = (string) getcwd();

        // Ensure build directory exists
        $buildDir = $projectRoot . '/build';
        if (!is_dir($buildDir)) {
            if (!mkdir($buildDir, 0755, true)) {
                $io->error('Failed to create build directory');
                return Command::FAILURE;
            }
        }

        // Check if box is available
        $boxPath = $projectRoot . '/vendor/bin/box';
        if (!file_exists($boxPath)) {
            $io->error('Box not found. Please run: composer install --dev');
            return Command::FAILURE;
        }

        // Generate listeners config
        $io->section('Generating listeners configuration');
        $this->generateListenersConfig($projectRoot);
        $io->success('Listeners configuration generated');

        $io->section('Building PHAR');
        $io->text('Using Box to compile seaman.phar...');

        // Workaround: Remove problematic vendor-hotfix directory temporarily
        $vendorHotfixPath = $projectRoot . '/vendor/humbug/php-scoper/vendor-hotfix';
        $vendorHotfixBackup = null;
        if (is_dir($vendorHotfixPath)) {
            $vendorHotfixBackup = $projectRoot . '/vendor/humbug/php-scoper/.vendor-hotfix-backup';
            rename($vendorHotfixPath, $vendorHotfixBackup);
        }

        // Run box compile
        $process = new Process(
            [$boxPath, 'compile', '--working-dir=' . $projectRoot],
            $projectRoot,
            null,
            null,
            300, // 5 minutes timeout
        );

        $process->run(function (string $type, string $buffer) use ($output): void {
            $output->write($buffer);
        });

        // Restore vendor-hotfix directory if it was backed up
        if ($vendorHotfixBackup !== null && is_dir($vendorHotfixBackup)) {
            rename($vendorHotfixBackup, $vendorHotfixPath);
        }

        if (!$process->isSuccessful()) {
            $io->error('Failed to build PHAR');
            $io->text($process->getErrorOutput());
            return Command::FAILURE;
        }

        $pharPath = $buildDir . '/seaman.phar';
        if (!file_exists($pharPath)) {
            $io->error('PHAR file was not created at expected location: ' . $pharPath);
            return Command::FAILURE;
        }

        $io->success('PHAR built successfully: ' . $pharPath);
        $io->text('You can now distribute build/seaman.phar');

        return Command::SUCCESS;
    }

    private function generateListenersConfig(string $projectRoot): void
    {
        $discovery = new \Seaman\EventListener\ListenerDiscovery($projectRoot . '/src/Listener');
        $listeners = $discovery->discover();

        $configDir = $projectRoot . '/config';
        if (!is_dir($configDir)) {
            mkdir($configDir, 0755, true);
        }

        $content = "<?php\n";
        $content .= "// Auto-generated by BuildCommand - do not edit manually\n\n";
        $content .= "declare(strict_types=1);\n\n";
        $content .= "use Seaman\\EventListener\\EventListenerMetadata;\n\n";
        $content .= "return [\n";

        foreach ($listeners as $metadata) {
            $content .= sprintf(
                "    new EventListenerMetadata(\n"
                . "        className: \\%s::class,\n"
                . "        event: '%s',\n"
                . "        priority: %d,\n"
                . "    ),\n",
                $metadata->className,
                $metadata->event,
                $metadata->priority,
            );
        }

        $content .= "];\n";

        file_put_contents($configDir . '/listeners.php', $content);
    }
}
